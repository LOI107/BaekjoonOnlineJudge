/*

< 기본사항 / 내 소스 >
- 시간 제한(2 초 / 0 MS)
- 메모리 제한(128 MB / 1984 KB)
- 문제를 푼 전원의 정답 비율(54.643 %)

< 문제 >
두 정수 N과 F가 주어진다. 지민이는 정수 N의 가장 뒤 두자리를 적절히 바꿔서 N을 F로 나누어 떨어지게 만드려고 한다. 
만약 가능한 것이 여러가지이면, 뒤 두자리를 가능하면 작게 만드려고 한다.
예를 들어, N=275이고, F=5이면, 답은 00이다. 200이 5로 나누어 떨어지기 때문이다. 
N=1021이고, F=11이면, 정답은 01인데, 1001이 11로 나누어떨어지기 때문이다.

< 입력 >
첫째 줄에 N, 둘째 줄에 F가 주어진다. N은 100보다 크거나 같고, 2,000,000,000보다 작거나 같은 자연수이다. 
F는 100보다 작거나 같은 자연수이다.

< 출력 >
첫째 줄에 마지막 두 자리를 모두 출력한다. 한자리이면 앞에 0을 추가해서 두 자리로 만들어야 한다.

< 알고리즘 분류 >
- 수학

*/

#include <iostream>

using namespace std;

int main(void)
{
	int N, F, index;

	cin >> N >> F;

	N = N - (N % 100); // 마지막 2자리를 버리기

	for (index = 0; index < 100; index++)
	{
		if ((N + index) % F == 0)
			break;
	}

	if (index < 10)
		cout << "0" << index << endl;
	
	else cout << index << endl;
}